class calculator:
    @staticmethod
    def add(a,b):
      return a+b
    
    @staticmethod
    def subtract(a,b):
      return a-b
    
    @staticmethod
    def multiply(a,b):
      return a*b
    
    @staticmethod
    def divide(a,b):
      return a//b
a=12
b=10
print(calculator.add(a,b))
print(calculator.subtract(a,b))
print(calculator.multiply(a,b))
print(calculator.divide(a,b))
      
